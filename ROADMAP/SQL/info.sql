-- INTRODUCCION A SQL

-- SQL, que significa lenguaje de consulta estructurado, es un lenguaje de programación que se utiliza para comunicarse y administrar bases de datos. SQL es un lenguaje estándar para manipular datos almacenados en sistemas de gestión de bases de datos relacionales (RDBMS) o para el procesamiento de flujos en un sistema de gestión de flujos de datos relacionales (RDSMS). Fue desarrollado por primera vez en la década de 1970 por IBM.

-- SQL consta de varios componentes, cada uno de los cuales tiene su propio propósito único en la comunicación de bases de datos:

-- Consultas: Este es el componente que permite recuperar datos de una base de datos. La declaración SELECT se usa más comúnmente para este propósito.

-- Lenguaje de definición de datos (DDL): le permite crear, modificar o eliminar bases de datos y sus objetos relacionados, como tablas, vistas, etc. Los comandos incluyen CREAR, ALTERAR, SOLTAR y TRUNCAR.

-- Lenguaje de manipulación de datos (DML): le permite administrar datos dentro de objetos de base de datos. Estos comandos incluyen SELECCIONAR, INSERTAR, ACTUALIZAR y ELIMINAR.

-- Lenguaje de control de datos (DCL): incluye comandos como GRANT y REVOKE, que se ocupan principalmente de derechos, permisos y otras tareas de gestión de nivel de control para el sistema de base de datos.

-- Las bases de datos SQL vienen en varias formas, como Oracle Database, Microsoft SQL Server y MySQL. A pesar de sus muchas diferencias, todas las bases de datos SQL utilizan los mismos comandos de lenguaje: SQL.
